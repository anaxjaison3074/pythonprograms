my_string = "Hello, World!"
slice1 = my_string[0:5]
print("Slice 1 (0:5):", slice1)
slice2 = my_string[7:12]
print("Slice 2 (7:12):", slice2)
slice3 = my_string[:]
print("Slice 3 (:):", slice3)
slice4 = my_string[-6:-1]
print("Slice 4 (-6:-1):", slice4)
slice5 = my_string[::2]
print("Slice 5 (::2):", slice5)
slice6 = my_string[::-1]
print("Slice 6 ([::-1]):", slice6)
